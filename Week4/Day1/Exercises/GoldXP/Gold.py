# What Is The Season ?
month = int(input("Enter a month:"))
if 3 <= month <= 5:
    print("Spring")
elif 6 <= month <= 8:
    print("Summer")
elif 9 <= month <= 11:
    print("Autumn")
elif month <= 12:
    print("Winter")

